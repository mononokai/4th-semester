public class Book {
    protected String title;
    protected String description;
    protected String author;
    protected String genre;
    protected String file;

    public Book(String title, String description, String author, String genre, String file) {
        this.title = title;
        this.description = description;
        this.author = author;
        this.genre = genre;
        this.file = file;
    }
}
